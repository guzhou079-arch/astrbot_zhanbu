def get_hexagram_symbol(upper_num: int, lower_num: int) -> str:
    """生成卦象符号 (从下到上显示)"""
    upper = BAGUA_BINARY[upper_num]
    lower = BAGUA_BINARY[lower_num]
    lines = list(lower) + list(upper)  # 从下到上
    symbols = []
    for line in reversed(lines):  # 显示从上到下
        symbols.append("⚊" if line == 1 else "⚋")
    return "\n".join(symbols)


def get_wuxing_relation(wx1: str, wx2: str) -> str:
    """判断两个五行的生克关系 (wx1 对 wx2)"""
    if wx1 == wx2:
        return "比和"
    if WUXING_SHENG[wx1] == wx2:
        return f"{wx1}生{wx2}"
    if WUXING_KE[wx1] == wx2:
        return f"{wx1}克{wx2}"
    if WUXING_SHENG[wx2] == wx1:
        return f"{wx2}生{wx1}"
    if WUXING_KE[wx2] == wx1:
        return f"{wx2}克{wx1}"
    return "无直接关系"
